				<h3>You Have a Role in the Body of Christ</h3>
				
				<p>“All of you together are Christ’s body, and each one of you  is a separate and necessary part of it.” (1 Corinthians 12:27 NLT)</p>

				<p>One reason why you need to be connected to a church family  is to fulfill your calling to serve other believers in practical ways. The  Bible says, “All of you together are Christ’s body, and each one of you is a  separate and necessary part of it” (1 Corinthians 12:27 NLT).</p>

				<p>Your service is desperately needed in the Body of Christ —  just ask any local church. Each of us has a role to play, and every role is  important. There is no small service to God; it all matters.</p>

				<p>Likewise, there are no insignificant ministries in the  church. Some are visible and some are behind the scenes, but all are valuable.  Small or hidden ministries often make the biggest difference. In my home, the  most important light is not the large chandelier in our dining room but the  little night-light that keeps me from stubbing my toe when I get up at night.  There is no correlation between size and significance. Every ministry matters,  because we are all dependent on each other to function.</p>

				<p>What happens when one part of your body fails to function?  You get sick. The rest of your body suffers. Imagine if your liver decided to  start living for itself: “I’m tired! I don’t want to serve the body anymore! I  want a year off just to be fed. I’ve got to do what’s best for me! Let some  other part take over.”</p>

				<p>What would happen? Your body would die. Today, thousands of  local churches are dying because of Christians who are unwilling to serve. They  sit on the sidelines as spectators, and the Body suffers.</p>

				<p>God calls you to a service far beyond anything you could  ever imagine. He created you for a  life of good deeds, which he has already prepared for you to do (Ephesians  2:10). Whenever you serve others in any way, you are actually serving God.</p>


